//for version

console.log("For version");

const x = 5;
let result = 0;

for (let i = 0; i <= x; i++) {
  result = result + i;
  console.log(result);
}
console.log(result);

//while version

let anotherResult = 0;
const y = 5;

console.log("While version");
let j = 0;

while (j <= y) {
  anotherResult = anotherResult + j;
  console.log(anotherResult);
  j++;
}
